#[derive(Clone, Eq, PartialEq, Hash)]
pub enum Field {
    Unknown(UnknownField),
    EnumField(EnumField),
    ValueField(ValueField),
}
impl Serialize for Field {
    fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        match self {
            Field::Unknown(value) => serializer.serialize_some(value),
            Field::EnumField(value) => serializer.serialize_str(&value.name),
            Field::ValueField(value) => serializer.serialize_str(&value.name),
        }
    }
}

#[derive(Clone)]
pub struct UnknownField {
    pub message_number: u16,
    pub field_number: u8,
}

impl Hash for UnknownField {
    fn hash<H: Hasher>(&self, state: &mut H) {
        self.message_number.hash(state);
        self.field_number.hash(state);
    }
}
impl PartialEq for UnknownField {
    fn eq(&self, other: &Self) -> bool {
        self.message_number == other.message_number &&
            self.field_number == other.field_number
    }
}

impl Eq for UnknownField {}
impl Serialize for UnknownField {
    fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        let serialized = format!("unknown {}/{}", self.message_number, self.field_number);

        serializer.serialize_str(serialized.as_str())
    }
}
#[derive(Clone)]
pub struct EnumField {
    pub name: &'static str,
    pub translate_enum: fn(&u32) -> String,
}

impl EnumField {
    const fn from(name: &'static str, translate_enum: fn(&u32) -> String) -> Self {
        EnumField {
            name,
            translate_enum,
        }
    }
}
impl Hash for EnumField {
    fn hash<H: Hasher>(&self, state: &mut H) {
        self.name.hash(state);
    }
}
impl PartialEq for EnumField {
    fn eq(&self, other: &Self) -> bool {
        self.name == other.name
    }
}

impl Eq for EnumField {}
impl Serialize for EnumField {
    fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        serializer.serialize_str(&self.name)
    }
}

#[derive(Clone)]
pub struct ValueField {
    pub name: &'static str,
}

impl Hash for ValueField {
    fn hash<H: Hasher>(&self, state: &mut H) {
        self.name.hash(state);
    }
}

impl PartialEq for ValueField {
    fn eq(&self, other: &Self) -> bool {
        self.name == other.name
    }
}

impl Eq for ValueField {}

impl Debug for ValueField {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        f.write_str(&self.name)
    }
}

impl Serialize for ValueField {
    fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        serializer.serialize_str(&self.name)
    }
}

impl ValueField {
    const fn from(name: &'static str) -> Self {
        ValueField { name }
    }
}

